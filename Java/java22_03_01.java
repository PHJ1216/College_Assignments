
public class java22_03_01 {

	public static void main(String[] args) {
		for(int i=0;i<4;i++) {
			for(int j=0;j<i;j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		//또는
		System.out.println("*");
		System.out.println("**");
		System.out.println("***");
	    
	}

}
